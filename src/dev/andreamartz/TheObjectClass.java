package dev.andreamartz;

public class TheObjectClass {
    public static void main(String[] args) {
        // The Object class is the root of all other classes
        // Every other class can use all the same methods that the Object class has on it

        Object name = "Jamil";  // or we could have said String name = "Jamil"
        System.out.println(name);
    }
}
